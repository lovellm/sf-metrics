CREATE OR REPLACE VIEW SF_METRICS.V_USERS (
  USER_ID
  , NAME
  , CREATED_ON
  , DELETED_ON
  , LOGIN_NAME
  , DISPLAY_NAME
  , FIRST_NAME
  , LAST_NAME
  , EMAIL
  , HAS_PASSWORD
  , COMMENT
  , DEFAULT_WAREHOUSE
  , DEFAULT_ROLE
  , LAST_SUCCESS_LOGIN
  , OWNER
  , TYPE
  , DATABASE_NAME
  , DATABASE_ID
  , SCHEMA_NAME
  , SCHEMA_ID
)
AS
SELECT
  USER_ID
  , NAME
  , CREATED_ON
  , DELETED_ON
  , LOGIN_NAME
  , DISPLAY_NAME
  , FIRST_NAME
  , LAST_NAME
  , EMAIL
  , HAS_PASSWORD
  , COMMENT
  , DEFAULT_WAREHOUSE
  , DEFAULT_ROLE
  , LAST_SUCCESS_LOGIN
  , OWNER
  , TYPE
  , DATABASE_NAME
  , DATABASE_ID
  , SCHEMA_NAME
  , SCHEMA_ID
FROM SF_METRICS.USERS
;
