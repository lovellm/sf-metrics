CREATE OR REPLACE VIEW SF_METRICS.V_WAREHOUSE_METERING_HISTORY (
  LOGDATE
  , START_TIME
  , END_TIME
  , WAREHOUSE_ID
  , WAREHOUSE_NAME
  , CREDITS_USED
  , CREDITS_USED_COMPUTE
  , CREDITS_USED_CLOUD_SERVICES
  , CREDITS_ATTRIBUTED_COMPUTE_QUERIES
)
AS
SELECT
  LOGDATE
  , START_TIME
  , END_TIME
  , WAREHOUSE_ID
  , WAREHOUSE_NAME
  , CREDITS_USED
  , CREDITS_USED_COMPUTE
  , CREDITS_USED_CLOUD_SERVICES
  , CREDITS_ATTRIBUTED_COMPUTE_QUERIES
FROM SF_METRICS.WAREHOUSE_METERING_HISTORY
;
