  ID
  , NAME
  , SCHEMA_ID
  , SCHEMA_NAME
  , DATABASE_ID
  , DATABASE_NAME
  , OWNER
  , ROW_COUNT
  , BYTES
  , RETENTION_TIME
  , CREATED
  , LAST_ALTERED
  , DELETED
  , COMMENT
  , OWNER_ROLE_TYPE
)
AS
SELECT
  ID
  , NAME
  , SCHEMA_ID
  , SCHEMA_NAME
  , DATABASE_ID
  , DATABASE_NAME
  , OWNER
  , ROW_COUNT
  , BYTES
  , RETENTION_TIME
  , CREATED
  , LAST_ALTERED
  , DELETED
  , COMMENT
  , OWNER_ROLE_TYPE
FROM SNOWFLAKE.ACCOUNT_USAGE.HYBRID_TABLES
